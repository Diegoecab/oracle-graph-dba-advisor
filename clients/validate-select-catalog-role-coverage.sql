DECLARE
  PROCEDURE run_count_check(p_name VARCHAR2, p_sql VARCHAR2) IS
    l_cnt NUMBER;
  BEGIN
    EXECUTE IMMEDIATE p_sql INTO l_cnt;
    DBMS_OUTPUT.PUT_LINE(RPAD(p_name, 42) || ' PASS rows=' || l_cnt);
  EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE(RPAD(p_name, 42) || ' FAIL ' || SQLERRM);
  END;
BEGIN
  run_count_check('V$SQL', 'SELECT COUNT(*) FROM (SELECT 1 FROM V$SQL WHERE ROWNUM <= 5)');
  run_count_check('V$SQLSTATS', 'SELECT COUNT(*) FROM (SELECT 1 FROM V$SQLSTATS WHERE ROWNUM <= 5)');
