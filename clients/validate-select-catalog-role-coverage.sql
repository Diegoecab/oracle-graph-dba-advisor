--------------------------------------------------------------------------------
-- validate-select-catalog-role-coverage.sql
--
-- Validates what SELECT_CATALOG_ROLE really covers for this skill.
--
-- This script is intentionally split into two layers:
--   1. Session-based SQL checks
--   2. Stored PL/SQL checks that mimic packaged Native MCP functions
--
-- Expected outcome:
--   - session-based reads should pass
--   - stored definer-rights PL/SQL should NOT be safely covered by the role
--
-- Required DEFINE variables before calling:
--   test_user
--   test_password
--   connect_alias
--   wallet_zip
--------------------------------------------------------------------------------

WHENEVER OSERROR EXIT FAILURE
WHENEVER SQLERROR EXIT SQL.SQLCODE

SET DEFINE ON
SET ECHO ON
SET FEEDBACK ON
SET HEADING ON
SET SERVEROUTPUT ON
SET PAGESIZE 200
SET LINESIZE 220
SET VERIFY OFF

SET CLOUDCONFIG &&wallet_zip
CONNECT &&test_user/"&&test_password"@&&connect_alias
SET SERVEROUTPUT ON

