CREATE OR REPLACE FUNCTION ga_role_dynamic_vsqlstats_fn
  RETURN NUMBER
AS
  l_cnt NUMBER;
BEGIN
  EXECUTE IMMEDIATE
    'SELECT COUNT(*) FROM V$SQLSTATS WHERE ROWNUM <= 5'
    INTO l_cnt;

  RETURN l_cnt;
END;
/

