CREATE OR REPLACE FUNCTION ga_role_static_vsqlstats_fn
  RETURN NUMBER
AS
  l_cnt NUMBER;
BEGIN
  SELECT COUNT(*)
  INTO l_cnt
  FROM V$SQLSTATS
  WHERE ROWNUM <= 5;

  RETURN l_cnt;
END;
/

SHOW ERRORS FUNCTION ga_role_static_vsqlstats_fn

